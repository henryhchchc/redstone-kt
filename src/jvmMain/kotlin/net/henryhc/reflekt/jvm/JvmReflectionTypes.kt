package net.henryhc.reflekt.jvm


/**
 * Denotes a type in JVM reflection APIs.
 */
typealias JvmType = java.lang.reflect.Type

/**
 * Denotes a class in JVM reflection APIs.
 */
typealias JvmClass = java.lang.Class<*>

/**
 * Denotes a type variable in JVM reflection APIs.
 */
typealias JvmTypeVariable = java.lang.reflect.TypeVariable<*>

/**
 * Denotes a parameterized reference to a generic type in JVM reflection APIs.
 */
typealias JvmParameterizedType = java.lang.reflect.ParameterizedType

